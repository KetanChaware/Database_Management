     public void init(){
       
        try{ 
            Class.forName("oracle.jdbc.driver.OracleDriver");
            Connection con=DriverManager.getConnection ("jdbc:oracle:thin:@127.0.0.1:1521:xe","system","iit");
        
            Statement stmt=con.createStatement();
            ResultSet rs=stmt.executeQuery("select stocks.product_no as no,prod_date,qty_produced,description,product_name from daily_production,stocks where daily_production.product_no = stocks.product_no group by stocks.product_no,prod_date,qty_produced,description,product_name order by prod_date desc");
        
            while(rs.next()){
                ((DefaultTableModel)jTable1.getModel()).addRow(new Object[]{rs.getDate("prod_date")+"", rs.getInt("no")+ "", rs.getString("product_name"), rs.getString("description"),rs.getInt("qty_produced")+""});
            }
            
            
        
        }catch(Exception e){JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);}
        
    } 
    
    
